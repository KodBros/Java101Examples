package j101e_2; 
public class Degiskenler {
    public static void main(String[] args) {
        byte a = 5; //byte değişken tipi -128 ile +127 arasındaki ufak tamsayı değerlerini içerir.
        short b = 1000; // short değişken tipi -32.768 ile +32767 arasındaki tamsayı değerlerini içerir.
        int c = 50000; //int değişken tipi çok büyük tamsayı değerlerini kapsar. 
        long d = 999999999; //long değişken tipi çok çok büyük tamsayı değerlerini kapsar.
        float e = 0.5f; //float değişken tipi virgüllü sayıları kapsar. Ayrıca sayının sonuna f koyarak float olduğunu belli ederiz.
        double f = 0.00001; //double değişken tipi daha hassas virgüllü sayıları ifade etmek için kullanılır.
        char g ='J'; // char değişken tipi karakter ifade etmek için kullanılır.
        boolean h = true; // boolean değişken tipi sadece true yada false değerlerini alır gelecek örneklerimizde daha fazla değineceğiz.
        String i = "Java Projem"; //String değişken tipi karakter dizilerini ifade etmek için kullanılır.   
    }
}
