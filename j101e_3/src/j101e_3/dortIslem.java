package j101e_3; 
public class dortIslem { 
    public static void main(String[] args) {
        int a = 5;
        int b = 5;

        System.out.println("a + b = "+(a+b));
        System.out.println("a - b = "+(a-b));
        System.out.println("a / b = "+(a/b));
        System.out.println("a * b = "+(a*b)); 
        System.out.println("a % b = "+(a%b));

        System.out.println("");
        
        float c = 0.9f;
        float d = 0.5f;
        
        System.out.println("c + d = "+(c+d));
        System.out.println("c - d = "+(c-d));
        System.out.println("c / d = "+(c/d));
        System.out.println("c * d = "+(c*d));
        System.out.println("c % d = "+(c%d)); 
    }   
}
